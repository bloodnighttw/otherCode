#include <iostream>
using namespace std;


int main(int argc, char** argv) {
	
	int ar[201][201];
	int i,j;
	//===================================== 變0 =================================================== 
	
	for(i=0;i<201;i++)
		for(j=0;j<201;j++)
			ar[i][j]=0;
	

	//=====================================算出對應位置============================================ 
	int x,y;
	for(x=-100;x<=100;x++){
		y=0.01*x*x*x+ 0.3*x*x+1*x+1;
		//y=x*x+2*x+1;
		//y=0.1*x*x*x+0.4*x*x+0.3*x;
		//y=2*x;
		if(y>100||y<-100)
			continue;
		ar[y+100][x+100]=1;
	}
	
	//=====================================算出對應位置=========================================== 
	
	
	//=====================================做出基準線標記=========================================	
	
	for(i=0;i<201;i++){
		
		if(ar[i][100]==0)
			ar[i][100]=2;
		if(ar[100][i]==0)		
			ar[100][i]=2;
	}
	
	
	
	//=====================================做出基準線標記========================================== 
	
	//========================================印出來=============================================== 
	
	for(i=200;i>=0;i--){
		for(j=0;j<201;j++){
			if(ar[i][j]==0)	
				cout << "  ";
			if(ar[i][j]==1)
				cout << "* ";
			if(ar[i][j]==2)
				cout << "= ";
		}		
		cout << "\n"; 
		
	}
	
	//========================================印出來=============================================== 
	return 0;
}
